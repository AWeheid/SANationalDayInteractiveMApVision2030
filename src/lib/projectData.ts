// Map region names to project data
export const regionProjects: Record<string, Project[]> = {};

projectsData.forEach(project => {
  if (!regionProjects[project.region]) {
    regionProjects[project.region] = [];
  }
  regionProjects[project.region].push(project);
});

// Check if a region has projects
export const hasProjects = (regionName: string): boolean => {
  return !!regionProjects[regionName] && regionProjects[regionName].length > 0;
};

// Get projects for a region
export const getRegionProjects = (regionName: string): Project[] => {
  return regionProjects[regionName] || [];
};
